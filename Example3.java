/*
 * Отличия между int и double
 */
class Example3 {
	public static void main(String[] args) {
		int v;    // объявление целочисленной переменной
		double x; // объявление переменной с плавующей точкой
		v = 10;   // инициализация переменной
		x = 10.0; // присваивание переменной значения
		
		System.out.println(v);
		System.out.println(x);
	}
}
