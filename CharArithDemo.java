/*
 * Демонстрация работы с типом char
 */
class CharArithDemo {
	public static void main(String[] args) {
		char ch;

		ch = 'X';		
		System.out.println("ch = " + ch);
		
		ch++;     // инкременирование ch
		System.out.println("ch = " + ch);

		ch = 90;  // присваивание ch значения Z
		System.out.println("ch = " + ch);

	}
}
