/*
 * Демонстрация работы с переменными
*/
class Example2 {
	public static void main(String[] args) {
		int myVar1; // объявление переменной
		int myVar2;

		myVar1 = 1024; // присваивание переменной значения

		System.out.println(myVar1);

		myVar2 = myVar1 / 2;

		System.out.println(myVar2);
	}
}
